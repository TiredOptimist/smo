# Задание 5. Замкнутая одноканальная СМО (система Энгсета)

import math

# НАЧАЛЬНЫЕ УСЛОВИЯ 
I = 16                      # количество источников заявок
K = 5                       # среднее число заявок от одного источника за месяц
T = 1.0                     # среднее время обслуживания одной заявки, дней
P = 98                      # требуемый процент исправных источников
DAYS_IN_MONTH = 30          # число дней в месяце

def engset_probabilities(i, rho):
    """
    Расчёт вероятностей состояний для замкнутой одноканальной СМО (формулы Энгсета)
    """
    total = 0.0
    term = 1.0          # это первое слагаемое (k=0)                     
    total += term
    for k in range(1, i + 1):
        term *= (i - k + 1) * rho
        total += term
    p0 = 1.0 / total

    probs = [0.0] * (i + 1)
    probs[0] = p0
    term = p0
    for k in range(1, i + 1):
        term *= (i - k + 1) * rho
        probs[k] = term
    return p0, probs

def main():
    # Пересчёт интенсивностей в единицах "заявка/день" 
    lambda_ = K / DAYS_IN_MONTH          # интенсивность поступления от одного источника, заявок/день
    mu = 1.0 / T                         # интенсивность обслуживания, заявок/день
    rho = lambda_ / mu                   # приведённая интенсивность

    print("ЗАМКНУТАЯ ОДНОКАНАЛЬНАЯ СМО (СИСТЕМА ЭНГСЕТА)")
    print("Исходные данные:")
    print(f"  Количество источников I = {I}")
    print(f"  Среднее число заявок от одного источника за месяц: K = {K} заявок/мес")
    print(f"  Среднее время обслуживания T = {T} дней")
    print(f"  Требуемый процент исправных источников P = {P}%")
    print(f"  Месяц принят равным {DAYS_IN_MONTH} дням")
    print(f"  lambda = {lambda_:.5f} заявок/день (от одного источника)")
    print(f"  mu = {mu:.5f} заявок/день")
    print(f"  rho = {rho:.5f}")

    # Расчёт вероятностей
    p0, probs = engset_probabilities(I, rho)

    # 1. Вероятность, что исправно не менее P% источников 
    max_unrepair = I * (1 - P / 100.0)          # максимально допустимое число неисправных
    prob_active_ge_P = probs[0] if max_unrepair < 1 else sum(probs[0:int(max_unrepair) + 1])

    # 2. Среднее число неисправных источников (в ремонте и в очереди) = L_сист
    L_sist = sum(k * p_k for k, p_k in enumerate(probs))

    # 3. Абсолютная пропускная способность A = mu * (1 - p0)
    A = mu * (1 - p0)

    # 4. Среднее время пребывания в системе (ремонт + ожидание)
    T_sist = L_sist / A if A > 0 else float('inf')
    T_ob = 1.0 / mu                  # среднее время обслуживания
    T_och = T_sist - T_ob            # среднее время ожидания

    print("\nРЕЗУЛЬТАТЫ:")
    print(f"  Вероятность того, что в исправном состоянии находится не менее {P}% источников:")
    print(f"  P(исправно >= {P}%) = {prob_active_ge_P:.5f}  ({prob_active_ge_P * 100:.3f}%)")
    print(f"  Среднее число неисправных источников L_сист = {L_sist:.3f}")
    print(f"  Абсолютная пропускная способность A = {A:.3f} заявок/день")
    print(f"  Среднее время пребывания в системе T_сист = {T_sist:.3f} дней = {T_sist * 24:.3f} часов")
    print(f"  Среднее время обслуживания T_об = {T_ob:.3f} дней")
    print(f"  Среднее время ожидания в очереди T_оч = {T_och:.3f} дней = {T_och * 24:.3f} часов")

    print("\nРаспределение вероятностей числа неисправных источников (состояний системы):")
    print("  k (неисправно)   p_k")
    for k in range(I + 1):
        if k <= 5 or k >= I - 2:
            print(f"       {k:3d}           {probs[k]:.5f}")
        elif k == 6:
            print("       ...            ...")


if __name__ == "__main__":
    main()