# Задание 3. Многоканальная СМО с ограниченной очередью

import math

# НАЧАЛЬНЫЕ УСЛОВИЯ (в соответствии с вариантом)
LAMBDA = 5.0          # интенсивность поступления заявок, заявок/час
T_SERVICE_MIN = 1.0   # среднее время обслуживания, минут
K = 20                # число каналов обслуживания
M = 2                 # максимальное число заявок в очереди (мест в очереди)
T_WORK = 8.0          # время работы СМО, часов
C_COST = 120.0        # стоимость обслуживания одной заявки, у.е.

def service_with_limited_queue(lambda_rate, mu, k, m):
    """
    Расчёт характеристик многоканальной СМО с ограниченной очередью
    Возвращает:
        p0         - вероятность свободного состояния
        P_otk      - вероятность отказа
        Q          - относительная пропускная способность
        A          - абсолютная пропускная способность
        L_och      - среднее число заявок в очереди
        L_ob       - среднее число заявок под обслуживанием
        L_sist     - среднее число заявок в системе
        T_och      - среднее время ожидания в очереди
        T_ob       - среднее время обслуживания
        T_sist     - среднее время пребывания в системе
        probs      - список предельных вероятностей состояний (от 0 до k+m)
    """
    rho = lambda_rate / mu                     # приведённая интенсивность
    rho_n = rho / k                            # ρ / n

    if abs(rho_n - 1.0) < 1e-10:
        raise ValueError("ρ/n = 1 => формулы не применимы")
    
    # 1. Вычисление p0
    # Сумма от i=0 до k: ρ^i / i!
    sum1 = 0.0
    for i in range(k + 1):
        sum1 += (rho ** i) / math.factorial(i)

    # Второе слагаемое знаменателя (аналитическое выражение для суммы геометрической прогрессии)
    sum2 = (rho ** (k + 1)) / (k * math.factorial(k)) * (1 - rho_n**m) / (1 - rho_n)

    p0 = 1.0 / (sum1 + sum2)

    # 2. Вероятность отказа (заявка теряется, если в системе k+m заявок)
    P_otk = (rho ** (k + m)) / (math.factorial(k) * (k ** m)) * p0

    # 3. Пропускные способности
    Q = 1.0 - P_otk                     # относительная
    A = lambda_rate * Q                 # абсолютная

    # 4. Среднее число заявок в очереди L_och
    factor = (rho ** (k + 1)) / (math.factorial(k) * k)
    bracket = 1.0 - (rho_n ** m) * (m + 1 - m * rho_n)
    denominator = (1.0 - rho_n) ** 2
    L_och = factor * bracket / denominator * p0

    # 5. Среднее число заявок под обслуживанием 
    L_ob = rho * Q

    # 6. Среднее число заявок в системе
    L_sist = L_och + L_ob

    # 7. Средние времена
    T_och = L_och / lambda_rate         # среднее время ожидания в очереди
    T_ob = 1.0 / mu                     # среднее время обслуживания
    T_sist = T_och + T_ob               # среднее время пребывания в системе

    # 8. Предельные вероятности состояний
    total_states = k + m
    probs = [0.0] * (total_states + 1)
    for i in range(total_states + 1):
        if i <= k:
            probs[i] = (rho ** i / math.factorial(i)) * p0
        else:
            r = i - k
            probs[i] = (rho ** i) / (math.factorial(k) * (k ** r)) * p0

    return p0, P_otk, Q, A, L_och, L_ob, L_sist, T_och, T_ob, T_sist, probs

# ОСНОВНАЯ ПРОГРАММА
def main():
    # Перевод времени обслуживания в часы
    t_service_hours = T_SERVICE_MIN / 60.0
    mu = 1.0 / t_service_hours          # интенсивность обслуживания, заявок/час
    rho = LAMBDA / mu                   # приведённая интенсивность

    # Расчёт характеристик
    (p0, P_otk, Q, A,
     L_och, L_ob, L_sist,
     T_och, T_ob, T_sist,
     probs) = service_with_limited_queue(LAMBDA, mu, K, M)

    # Потеря выручки
    S_loss = C_COST * LAMBDA * P_otk * T_WORK

    print("МНОГОКАНАЛЬНАЯ СМО С ОГРАНИЧЕННОЙ ОЧЕРЕДЬЮ")
    print("Исходные данные:")
    print(f"  lambda = {LAMBDA} заявок/час")
    print(f"  t_обсл = {T_SERVICE_MIN} мин = {t_service_hours:.5f} час")
    print(f"  mu = {mu:.5f} заявок/час")
    print(f"  k = {K} каналов")
    print(f"  m = {M} мест в очереди")
    print(f"  T = {T_WORK} час")
    print(f"  C = {C_COST} у.е./заявка")
    print(f"  rho = {rho:.5f}")

    print("-" * 70)
    print("ПРЕДЕЛЬНЫЕ ВЕРОЯТНОСТИ СОСТОЯНИЙ:")
    print("-" * 70)
    # Вывод первых нескольких и последних состояний для компактности
    print("  Состояние (i)    Вероятность p_i")
    for i in range(min(K + M + 1, 10)):
        print(f"       {i:2d}            {probs[i]:.3f}")
    if K + M >= 10:
        print("       ...               ...")
        for i in range(max(K + M - 3, 10), K + M + 1):
            print(f"       {i:2d}            {probs[i]:.3f}")
    print("-" * 70)

    print("ОСНОВНЫЕ ХАРАКТЕРИСТИКИ СМО:")
    print("-" * 70)
    print(f"  Вероятность отказа P_отк               = {P_otk:.3f}  ({P_otk*100:.3f}%)")
    print(f"  Относительная пропускная способность Q = {Q:.3f}  ({Q*100:.3f}%)")
    print(f"  Абсолютная пропускная способность A    = {A:.3f} заявок/час")
    print(f"  Среднее число заявок в очереди L_оч    = {L_och:.3f}")
    print(f"  Среднее число заявок под обслуживанием L_об = {L_ob:.3f}")
    print(f"  Среднее число заявок в системе L_сист  = {L_sist:.3f}")
    print(f"  Среднее время ожидания в очереди T_оч  = {T_och:.3f} час = {T_och*60:.3f} мин")
    print(f"  Среднее время обслуживания T_об        = {T_ob:.3f} час = {T_ob*60:.3f} мин")
    print(f"  Среднее время пребывания в системе T_сист = {T_sist:.3f} час = {T_sist*60:.3f} мин")
    print("-" * 70)

    print("ЭКОНОМИЧЕСКИЙ ПОКАЗАТЕЛЬ:")
    print("-" * 70)
    print(f"  Потеря выручки за {T_WORK} час работы: S_потерь = {S_loss:.3f} у.е.")

if __name__ == "__main__":
    main()